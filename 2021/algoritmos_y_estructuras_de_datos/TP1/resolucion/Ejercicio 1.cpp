#include <iostream>
using namespace std;
void main() {
    int Valor1, Valor2;
    cout << "Ingrese un primer numero: ";
    cin >> Valor1;

    cout << "Ingrese un segundo numero: ";
    cin >> Valor2;

    cout << "La suma de los numeros es: ";
    cout << Valor1 + Valor2 << endl;
    cout << "La resta del primero menos el segundo es: ";
    cout << Valor1 - Valor2 << endl;
    cout << "El producto de los numeros es: ";
    cout << Valor1 * Valor2 << endl;
}